public class Country {

    String name;
    long population;
    String capital;
    String formOfPower;
    double area;

    public Country(String name, long population, String capital, String formOfPower, double area) {
        this.name = name;
        this.population = population;
        this.capital = capital;
        this.formOfPower = formOfPower;
        this.area = area;
    }

    public Country() {
    }

}
